def resolve_wms_band(score: int) -> Dict[str, Any]:
    """Итог по числу пунктов со статусом «Готово» (0–10)."""
    s = max(0, min(10, int(score)))
    if s <= 3:
        return {
            "code": "red",
            "title": "Базовый уровень",
            "body": (
                "На данном этапе автоматизация через полноценную WMS может быть избыточна. Рекомендуем "
                "сфокусироваться на наведении операционного порядка: ввести адресное хранение, актуализировать учёт, "
                "разметить зоны. Эти меры сами по себе повысят эффективность. Вернитесь к оценке через 3–6 месяцев — "
                "после стабилизации процессов."
            ),
        }
    if s <= 6:
        return {
            "code": "yellow",
            "title": "Частичная готовность",
            "body": (
                "Фундамент есть, но есть и зоны роста. Целесообразно выделить 2–3 ключевых пункта, которые "
                "сдерживают внедрение (например, штрихкодирование или интеграция), и проработать их в формате "
                "пилота или подготовительного проекта. Это снизит риски и упростит старт основной фазы внедрения."
            ),
        }
    if s <= 8:
        return {
            "code": "green",
            "title": "Готовность к старту",
            "body": (
                "Вы можете начинать диалог с интеграторами WMS. Незакрытые пункты стоит зафиксировать как риски "
                "в плане проекта и предусмотреть на их устранение время и ресурсы на ранних этапах внедрения. "
                "Такой подход позволит избежать срывов сроков в активной фазе работ."
            ),
        }
    return {
        "code": "optimal",
        "title": "Оптимальная готовность",
        "body": (
            "Условия для внедрения благоприятные. Можно переходить к тендеру и выбору интегратора. Рекомендуем "
            "параллельно подготовить внутреннюю дорожную карту: утвердить спонсора, сформировать рабочую группу и "
            "синхронизировать ожидания по срокам и бюджету."
        ),
    }


def count_ready_answers(status_by_item: Dict[int, Optional[str]]) -> int:
    return sum(1 for n in range(1, 11) if status_by_item.get(n) == "ready")
