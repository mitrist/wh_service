"""
20 основных вопросов самоаудита + условный q21 (ветка от «бумажные списки»).
Тексты вопросов и микро-комментарии — по comments.md; структура этапов — по quiz.md.
Веса и критерии — по logic.md (поле criterion = ключ в API/модели).
"""
from __future__ import annotations

from dataclasses import dataclass
from typing import List, Optional


@dataclass(frozen=True)
class AuditOption:
    option_id: str
    label: str
    ball_percent: int
    after_answer_comment: str = ""


@dataclass(frozen=True)
class AuditQuestion:
    number: int
    title: str
    weight: int
    criterion: Optional[str]
    check_note: str
    impact_text: Optional[str] = None
    options: Optional[List[AuditOption]] = None

    def is_scored(self) -> bool:
        return self.options is not None and self.weight > 0

    def get_option(self, option_id: str) -> AuditOption:
        if not self.options:
            raise ValueError(f"Question {self.number} has no options.")
        for opt in self.options:
            if opt.option_id == option_id:
                return opt
        raise ValueError(f"Unknown option_id={option_id} for question {self.number}.")


def _c(text: str) -> str:
    return (text or "").strip()


